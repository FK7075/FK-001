package com.fk.javaBean;

public class Users {
	public String Uid;
	public String Password;
	public int Perm;
	public String getUid() {
		return Uid;
	}
	public void setUid(String uid) {
		Uid = uid;
	}
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		Password = password;
	}
	public int getPerm() {
		return Perm;
	}
	public void setPerm(int perm) {
		Perm = perm;
	}

}
